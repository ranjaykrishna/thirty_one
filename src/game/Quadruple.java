package game;

public class Quadruple {
	public int first;
	public int second;
	public int third;
	public int fourth;
	public Quadruple(){
		first = 0;
		second = 0;
		third = 0;
		fourth = 0;
	}
	public Quadruple(int f, int s, int t, int ff){
		first = f;
		second = s;
		third = t;
		fourth = ff;
	}
}
